/** Standard Google Fonts CSS2 stylesheet URL — see
 * https://developers.google.com/fonts/docs/css2 */
export function googleFontsUrl(fontName: string, weights: number[] = [400, 500, 600, 700]): string {
  const family = fontName.replace(/ /g, "+");
  const weightParam = weights.sort((a, b) => a - b).join(";");
  return `https://fonts.googleapis.com/css2?family=${family}:wght@${weightParam}&display=swap`;
}
